import caculate_matrix_n as cn
import numpy as np
import sympy as sp

A1,B1 = cn.getA_B(2)
A2,B2 = cn.getA_B(1)
A = np.r_[A1,A2]
B = np.r_[B1,B2]
selected_row = [6, 8, 9, 10]
a = sp.Matrix(A[selected_row, :])
b = sp.Matrix(B[selected_row, :])
X = a.solve(b)
print(X.T)

